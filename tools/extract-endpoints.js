#!/usr/bin/env node
'use strict';

/**
 * Parse data/spike.har and emit src/kooroo/endpoints.json + a human-readable
 * summary. Heuristics only — review the summary and edit endpoints.json
 * if anything looks off.
 */

const fs = require('fs');
const path = require('path');
const config = require('../src/config');

const HAR_PATH = path.join(config.dataDir, 'spike.har');
const OUT_JSON = path.join(__dirname, '..', 'src', 'kooroo', 'endpoints.json');
const OUT_SUMMARY = path.join(config.dataDir, 'spike-summary.txt');

function main() {
  if (!fs.existsSync(HAR_PATH)) {
    console.error(`No HAR at ${HAR_PATH}. Run \`npm run spike\` first.`);
    process.exit(2);
  }
  const har = JSON.parse(fs.readFileSync(HAR_PATH, 'utf8'));
  const entries = har.log?.entries || [];
  const byHost = new Map();
  const seen = new Set();
  const endpoints = [];

  for (const e of entries) {
    const url = new URL(e.request.url);
    const host = url.host;
    if (!byHost.has(host)) byHost.set(host, []);
    byHost.get(host).push(e);

    const key = `${e.request.method} ${url.pathname}`;
    if (seen.has(key)) continue;
    seen.add(key);
    const ct = e.response?.content?.mimeType || '';
    const status = e.response?.status;
    endpoints.push({
      method: e.request.method,
      path: url.pathname,
      status,
      contentType: ct,
      sampleQuery: url.search.slice(1),
      requestHeaders: e.request.headers?.filter((h) => /^(cookie|authorization|x-|csrf|referer|origin)/i.test(h.name)).map((h) => ({ name: h.name, value: h.value })),
      responseHeaders: e.response?.headers?.filter((h) => /^(set-cookie|x-|csrf)/i.test(h.name)).map((h) => ({ name: h.name, value: h.value })),
      requestBodyShape: e.request?.postData?.text ? e.request.postData.text.slice(0, 240) : null,
      responseBodyShape: e.response?.content?.text ? String(e.response.content.text).slice(0, 240) : null,
    });
  }

  // Heuristics for auth + sessions
  const cookies = new Set();
  const cookieHeaders = [];
  for (const e of entries) {
    for (const h of e.response?.headers || []) {
      if (/^set-cookie$/i.test(h.name)) {
        const name = (h.value || '').split('=')[0];
        if (name) cookies.add(name);
        cookieHeaders.push({ url: e.request.url, value: h.value });
      }
    }
  }

  const loginGuess = endpoints.find((e) => /login|signin|auth/i.test(e.path)) || endpoints.find((e) => e.method === 'POST' && /password|email|username/i.test(e.requestBodyShape || ''));
  const probeGuess = endpoints.find((e) => e.method === 'GET' && /me|session|profile|user/i.test(e.path));
  const availGuess = endpoints.find((e) => e.method === 'GET' && /availab|slot|calendar|timeslot/i.test(e.path));
  const bookGuess = endpoints.find((e) => e.method === 'POST' && /book|reserve|reservation|slot/i.test(e.path));
  const cancelGuess = endpoints.find((e) => e.method === 'POST' && /cancel/i.test(e.path));

  const out = {
    version: 1,
    baseUrl: `https://${[...byHost.keys()][0] || 'kooroora.asn.au'}`,
    auth: {
      login: loginGuess ? { method: loginGuess.method, path: loginGuess.path, contentType: loginGuess.contentType, bodyShape: /application\/json/i.test(loginGuess.contentType) ? 'json' : 'form', headers: loginGuess.requestHeaders || [] } : { method: null, path: null },
      logout: { method: null, path: null },
      sessionProbe: probeGuess ? { method: probeGuess.method, path: probeGuess.path, expectedStatus: probeGuess.status } : { method: 'GET', path: null },
    },
    session: {
      type: cookies.size > 0 ? 'cookie' : 'unknown',
      cookieNames: [...cookies],
      bearerHeader: null,
      csrfHeader: null,
      csrfCookie: null,
    },
    endpoints,
    notes: 'Auto-generated by tools/extract-endpoints.js. Review and edit before relying on it.',
  };

  fs.writeFileSync(OUT_JSON, JSON.stringify(out, null, 2));
  console.log('Wrote', OUT_JSON);
  console.log('  cookies seen:', [...cookies].join(', ') || '(none)');
  console.log('  candidate login   :', loginGuess ? `${loginGuess.method} ${loginGuess.path}` : '(none)');
  console.log('  candidate probe   :', probeGuess ? `${probeGuess.method} ${probeGuess.path}` : '(none)');
  console.log('  candidate avail   :', availGuess ? `${availGuess.method} ${availGuess.path}` : '(none)');
  console.log('  candidate book    :', bookGuess ? `${bookGuess.method} ${bookGuess.path}` : '(none)');
  console.log('  candidate cancel  :', cancelGuess ? `${cancelGuess.method} ${cancelGuess.path}` : '(none)');

  // human summary
  const lines = [];
  lines.push(`HAR: ${HAR_PATH}`);
  lines.push(`Hosts: ${[...byHost.keys()].join(', ')}`);
  lines.push(`Total entries: ${entries.length}`);
  lines.push(`Distinct method+path: ${endpoints.length}`);
  lines.push('');
  lines.push('Cookies set:');
  for (const c of cookieHeaders) lines.push(`  ${c.url}\n    ${c.value}`);
  lines.push('');
  lines.push('Endpoints (method path -> status, ct):');
  for (const e of endpoints) lines.push(`  ${e.method.padEnd(6)} ${e.path}  -> ${e.status}  ${e.contentType}`);
  fs.writeFileSync(OUT_SUMMARY, lines.join('\n'));
  console.log('Wrote', OUT_SUMMARY);
}

main();
